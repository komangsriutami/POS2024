                  <h3>Create Nota</h3>
                  <ul>
                     <li>Klik tombol <span class="text-warning"><b>Tambah Pembelian</b></span>. Setiap menambah item baru, secara otomatis akan tersimpan.</li>
                     <li>Pastikan data nota yang wajib diisi telah diinputkan sebelum menginputkan item pembelian.</li>
                  </ul>
                  <br>
                  <h3>Edit Nota</h3>
                  <ul>
                     <li>Klik tombol <span class="text-warning"><b>Edit Nota</b></span> pada kolom <span class="text-warning"><b>Action</b></span>.</li>
                     <li>Edit data nota, hanya dapat dilakukan pada data yang bukan item penjualan. Anda dapat melakukan perubahan data dan mengklik tombol <span class="text-warning"><b>Simpan</b></span> untuk menyimpan data.</li>
                     <li>Sedangkan perubahan data item pembelian hanya dengan kondisi nota belum disign/ditanda tangani/nota telah dilunasi. Jika nota sudah dittd, maka hubungi ka outlet untuk membatalkan ttd. Kemudian anda dapat menghapus item yang salah [baca pada bagian <span class="text-info">Hapus Item</span>], dan menginput kembali item data yang benar.</li>
                  </ul>
                  <br>
                  <h3>Hapus Nota</h3>
                  <ul>
                     <li>Klik tombol <span class="text-warning"><b>Hapus Nota</b></span> pada kolom <span class="text-warning"><b>Action</b></span>.</li>
                     <li>Proses ini dapat dilakukan pada dengan kondisi nota belum disign/ditanda tangani/nota telah dilunasi. Jika nota sudah dittd/lunas, maka hubungi ka outlet untuk membatalkan ttd. Kemudian tombol hapus pada nota akan muncul, dan anda dapat menghapus nota yang salah.</li>
                     <li>Proses ini akan mengurangi stok dari seluruh barang yang termuat pada nota sesuai jumlah yang di catat di pembelian.</li>
                  </ul>
                  <br>
                  <h3>Faktur Asli</h3>
                  <ul>
                     <li>Klik tombol <span class="text-warning"><b>Faktur Asli</b></span> pada kolom <span class="text-warning"><b>Action</b></span>.</li>
                     <li>Proses ini dilakukan untuk menandai bahwa faktur asli pembelian telah diterima oleh outlet.</li>
                  </ul>

                  <br><hr><br>

                  <h3>Edit Item</h3>
                  <ul>
                     <li>Proses ini dapat dilakukan pada dengan kondisi nota belum disign/ditanda tangani/nota telah dilunasi. Jika nota sudah dittd/lunas, maka hubungi ka outlet untuk membatalkan ttd. Kemudian anda dapat menghapus item yang salah [baca pada bagian <span class="text-info">Hapus Item</span>], dan menginput kembali item data yang benar.</li>
                  </ul>
                  <br>
                  <h3>Hapus Item</h3>
                  <ul>
                     <li>Klik tombol <span class="text-warning"><b>Hapus</b></span> pada kolom <span class="text-warning"><b>Action</b></span> tabel list item pembelian.</li>
                     <li>Proses ini dapat dilakukan pada dengan kondisi nota belum disign/ditanda tangani/nota telah dilunasi. Jika nota sudah dittd/lunas, maka hubungi ka outlet untuk membatalkan ttd. Kemudian tombol hapus akan muncul, dan anda dapat menghapus item yang salah.</li>
                     <li>Proses ini akan mengurangi stok dari barang yang dihapus.</li>
                  </ul>

                  <br>
